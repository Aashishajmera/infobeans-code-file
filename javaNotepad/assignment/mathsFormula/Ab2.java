class Ab2 {
	public static void main(String[] args) {

		byte a = 5;
		byte b = 4;
		byte c = (byte) (a + b);
		System.out.println("(a+b)^2 value is: " + (c * c));

		// second type
		byte d = 4;
		byte e = 8;
		int f = (d * d) + (e * e) + (2 * d * e);
		System.out.println("(a+b)^2 value is: " + f);

	}
}