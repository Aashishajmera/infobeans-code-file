public class logicalOperator {
    public static void main(String[] args) {
        byte num1 = 4;
        byte num2 = 8;
        
        // logical AND 
        System.out.println((num1 > 2) && (num2 > 3));

        // logical OR
        System.out.println((num1 > 2) || (num2 > 3));

        // 
    }
}
