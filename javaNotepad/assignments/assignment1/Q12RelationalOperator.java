// 12. Perform all relational operation

public class Q12RelationalOperator {
    public static void main(String[] args) {

        // Realation Op (>, <, >=, <=, ==, !=)

        byte num1 = 4;
        byte num2 = 3;

        // greater than
        System.out.println(num1 > num2);

        // less than
        System.out.println(num1 < num2);

        // greater than or equal to
        System.out.println(num1 >= num2);

        // less than or equal to
        System.out.println(num1 <= num2);

        // equal to
        System.out.println(num1 == num2);

        // not equal to
        System.out.println(num1 != num2);
    }
}
