// 10. Write to program to find out volume of cylinder

public class Q10VolumeOfCylinder {
    public static void main(String[] args) {
        int radius = 5;
        float pi = 3.1415f;
        int height = 7;
        float volumeOfCylinder = (pi * radius * radius * height);
        System.out.println("volume of cylinder is: " + volumeOfCylinder);
        
    }
}
