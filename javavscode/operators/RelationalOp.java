package javavscode.operators;

public class RelationalOp {
    public static void main(String[] args) {
        // comparison operator
        int a = 34;
        int b = 9;
        System.out.println(a >= b);

        System.out.println(a <= b);

        System.out.println(a > b);

        System.out.println(a < b);

        System.out.println(a != b);

        System.out.println(a == b);

    }
}
