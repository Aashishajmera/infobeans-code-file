package javavscode.datatype;

public class AllDataType {
    public static void main(String[] args) {
        // integer
        int num1 = 23;
        System.out.println(num1);

        // float 
        float num2 = 32.3f;
        System.out.println(num2);

        // double  
        double num3 = 33.543;
        System.out.println(num3);
        
       
    }
}