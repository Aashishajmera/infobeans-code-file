public class CheckShift{
    public static void main(String[] args) {
        int k = 5;

        System.out.println(k<<2);   //20 

        System.out.println(k);    // <<<   >>>

        System.out.println(k>>3);


        System.out.println(1.1/1.0);

        System.out.println(1.1/0.1);

    }
}